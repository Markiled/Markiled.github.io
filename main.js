$(document).ready(function() {
  $('#toggle').click(function() {
    var elem = $('#toggle').text();
    if (elem == "Read more") {
      //Stuff to do when btn is in the read more state
      $('#toggle').text('Read less');
      $('#more').slideDown();
    } else {
      //Stuff to do when btn is in the read less state
      $('#toggle').text('Read more');
      $('#more').slideUp();
    }
  });
});

